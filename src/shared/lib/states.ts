const NAME_BY_CODE = new Map(INDIAN_STATES.map((s) => [s.code, s.name]));

/** State display name for a GST state code. */
export function stateName(code: string | null | undefined): string {
  return code ? (NAME_BY_CODE.get(code) ?? '') : '';
}

